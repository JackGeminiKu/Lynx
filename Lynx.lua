function Draw()
    if DRAW_ENABLED and CanDraw() then
        LibDraw.clearCanvas()
        local pX, pY, pZ = Wow.ObjectPosition("player")

        -- Draw Status String
        if DRAW_TEXT_ENABLED then
            -- Debug message
