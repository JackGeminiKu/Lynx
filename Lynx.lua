local function MoveFacingNPC(obj, div, into)
    local px, py, pz = ObjectPosition("player")
    local xx, yy, zz = ObjectPosition(obj)
    local ddx = math.abs(xx - px)
    local ddy = math.abs(yy - py)
    local ddz = math.abs(yy - pz)

    local ddx = (px - xx) / div
    local ddy = (py - yy) / div
    local ddz = (pz - zz) / div
    local mx, my, mz

    if into then
        if ddx < 0 then
            mx = px + math.abs(ddx)
        else
            mx = px - math.abs(ddx)
        end
        if ddy < 0 then
            my = py + math.abs(ddy)
        else
            my = py - math.abs(ddy)
        end
        if ddz < 0 then
            mz = pz + math.abs(ddz)
        else
            mz = pz - math.abs(ddz)
        end
    else
        if ddx < 0 then
            mx = px - math.abs(ddx)
        else
            mx = px + math.abs(ddx)
        end
        if ddy < 0 then
            my = py - math.abs(ddy)
        else
            my = py + math.abs(ddy)
        end
        if ddz < 0 then
            mz = pz - math.abs(ddz)
        else
            mz = pz + math.abs(ddz)
        end
    end

    MoveTo(mx, my, mz)
end

