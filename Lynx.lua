        SellTick = SellTick + 1

        if Wow.CanMerchantRepair() then
            Wow.RepairAllItems()
        end
        if SellTick == 5 then
            SellComplete = true
            CanMail = true
            SellTick = 0
        end
        Sleep(6)
    end
end

