function MoveFacingObject(obj, div, into)
    local px, py, pz = Wow.ObjectPosition("player")
    local ox, oy, oz = Wow.ObjectPosition(obj)
    local dx = (px - ox) / div
    local dy = (py - oy) / div
    local dz = (pz - oz) / div
    local mx, my, mz

    if into then
        if dx < 0 then
            mx = px + math.abs(dx)
        else
            mx = px - math.abs(dx)
        end
        if dy < 0 then
            my = py + math.abs(dy)
        else
            my = py - math.abs(dy)
        end
        if dz < 0 then
            mz = pz + math.abs(dz)
        else
            mz = pz - math.abs(dz)
        end
    else
        if dx < 0 then
            mx = px - math.abs(dx)
        else
            mx = px + math.abs(dx)
        end
        if dy < 0 then
            my = py - math.abs(dy)
        else
            my = py + math.abs(dy)
        end
        if dz < 0 then
            mz = pz - math.abs(dz)
        else
            mz = pz + math.abs(dz)
        end
    end

    Wow.MoveTo(mx, my, mz)
end

