            if mana < 10 and enemyHP > 6 and Wow.IsCastable("Evocation") and HasManaGem() == false then
                Wow.CastSpellByName("Evocation")
                Spell = "Evocation"
                if hp > 75 then
                    Sleep(6.1) -- almost full duration
                elseif hp > 50 then
                    Sleep(4.1)
                else
                    Sleep(2.1)
                end
                return
            elseif mana > 40 or hp < 20 then
