function IsPolymorphUsed()
    local objCount = Wow.GetObjectCount()
    for i = 1, objCount do
        local obj = Wow.GetObjectWithIndex(i)
        if obj ~= nil then
            if Wow.UnitIsEnemy("player", obj) and Wow.UnitIsDead(obj) == false then
